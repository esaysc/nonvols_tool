import pandas as pd

def fix_excel_data():
    file_path = '贵平2022年大豆发放花名册_修复版.xlsx'
    df = pd.read_excel(file_path)
    
    # 1. 修正 10 组末尾的错误识别 (C -> 5)
    df.loc[df['姓名'] == '王久奎', '数量(公斤)'] = '5'
    df.loc[df['姓名'] == '王金文', '数量(公斤)'] = '5'
    
    # 2. 准备 11 组和 12 组的数据 (根据图片内容)
    # 图片显示 10 组末尾是 王本辉, 王九波, 王述文, 王九方
    # 然后是 11 组: 李淑华, 汤思明, 汪淑华, 陈述文, 汤建明
    # 然后是 12 组: 王伯元, 李淑先, 王水明, 王金明
    
    group_11_12_data = [
        ['中岗村', '10组', '王本辉', 6.0, 21.5, None, '公斤'],
        ['中岗村', '10组', '王九波', 3.3, 10.0, None, '公斤'],
        ['中岗村', '10组', '王述文', 5.8, 17.5, None, '公斤'],
        ['中岗村', '10组', '王九方', 3.3, 10.0, None, '公斤'],
        ['中岗村', '11组', '李淑华', 1.3, 4.0, None, '公斤'],
        ['中岗村', '11组', '汤思明', 5.3, 16.0, None, '公斤'],
        ['中岗村', '11组', '汪淑华', 1.7, 5.0, None, '公斤'],
        ['中岗村', '11组', '陈述文', 6.7, 20.0, None, '公斤'],
        ['中岗村', '11组', '汤建明', 1.0, 3.0, None, '公斤'],
        ['中岗村', '12组', '王伯元', 9.3, 28.0, None, '公斤'],
        ['中岗村', '12组', '李淑先', 4.2, 12.5, None, '公斤'],
        ['中岗村', '12组', '王水明', 3.0, 9.0, None, '公斤'],
        ['中岗村', '12组', '王金明', 11.7, 35.0, None, '公斤'],
    ]
    
    new_rows = pd.DataFrame(group_11_12_data, columns=df.columns)
    
    # 3. 找到插入位置 (在 10 组 '王守良' 之后)
    idx = df[df['姓名'] == '王守良'].index[0]
    
    # 4. 拼接数据
    df_final = pd.concat([df.iloc[:idx+1], new_rows, df.iloc[idx+1:]]).reset_index(drop=True)
    
    # 5. 移除重复或错误的 12 组行 (如果有)
    # 之前看到 249 行有一个 '吴淑彬' 12组，保留它，但确保它在正确的位置
    
    # 保存
    df_final.to_excel('贵平2022年大豆发放花名册_最终修复版.xlsx', index=False)
    print("最终修复版已保存。总行数:", len(df_final))

if __name__ == "__main__":
    fix_excel_data()
