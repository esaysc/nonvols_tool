import os
import pandas as pd

# -------------------------- 配置区 --------------------------
# 每列的行数（可修改为29/30/其他数值）
ROWS_PER_COLUMN = 30
# 生成的Excel文件名
OUTPUT_FILE = "姓名排版结果_v2.xlsx"
# -----------------------------------------------------------


def arrange_names_to_excel(raw_names_str, rows_per_col, output_file):
    """
    将原始文本格式的姓名按指定行数换列，生成Excel文件
    """
    # 1. 处理原始姓名文本，转为干净的列表
    # 使用 strip() 去除首尾空格及换行，并过滤空行
    raw_list = [name.strip() for name in raw_names_str.splitlines() if name.strip()]

    # 保留所有姓名（包括重复项），仅过滤纯空行
    names_list = raw_list

    if not names_list:
        print("❌ 错误：姓名列表为空，请检查输入数据。")
        return

    total_names = len(names_list)

    # 2. 使用列表切片快速分块
    # 将列表按 rows_per_col 大小切分成多个子列表（每一列的数据）
    columns = [names_list[i:i + rows_per_col] for i in range(0, total_names, rows_per_col)]

    # 3. 使用 pandas 构建 DataFrame
    # 直接从列数据创建，保持 NaN 不填充，这样 Excel 不会统计空单元格
    df = pd.DataFrame({i: pd.Series(col) for i, col in enumerate(columns)})

    # 4. 生成Excel文件
    try:
        # 不再 fillna("")，让 Excel 识别为真正的空单元格
        df.to_excel(output_file, index=False, header=False)

        # 5. 输出统计信息
        print("=" * 50)
        print("✅ 生成成功！")
        print(f"📂 文件路径：{os.path.abspath(output_file)}")
        print("📊 统计信息：")
        print(f"   - 总姓名数：{total_names} 个")
        print(f"   - 排版规则：每 {rows_per_col} 行换一列")
        print(f"   - 生成列数：{len(columns)} 列")
        print("=" * 50)
    except Exception as e:
        print(f"❌ 写入文件失败: {e}")


