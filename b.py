import os
import math
import pandas as pd

# -------------------------- 配置区 --------------------------
# 固定列数
FIXED_COLUMNS = 9
# 生成的Excel文件名
OUTPUT_FILE = "姓名排版结果_9列.xlsx"
# -----------------------------------------------------------


def arrange_names_to_excel(raw_names_str, fixed_cols, output_file):
    """
    将原始文本格式的姓名按固定列数排版，行数自动计算
    """
    # 1. 处理原始姓名文本，转为干净的列表
    raw_list = [name.strip() for name in raw_names_str.splitlines() if name.strip()]

    # 保留所有姓名（包括重复项）
    names_list = raw_list

    if not names_list:
        print("❌ 错误：姓名列表为空，请检查输入数据。")
        return

    total_names = len(names_list)

    # 2. 计算每列需要的行数 (n行)，向上取整
    rows_per_col = math.ceil(total_names / fixed_cols)

    # 3. 使用列表切片分块（每一块代表一列）
    columns = [names_list[i:i + rows_per_col] for i in range(0, total_names, rows_per_col)]

    # 4. 使用 pandas 构建 DataFrame
    # 直接从列数据创建，保持 NaN 不填充，这样 Excel 不会统计空单元格
    df = pd.DataFrame({i: pd.Series(col) for i, col in enumerate(columns)})

    # 5. 生成Excel文件
    try:
        df.to_excel(output_file, index=False, header=False)

        # 6. 输出统计信息
        print("=" * 50)
        print("✅ 生成成功！")
        print(f"📂 文件路径：{os.path.abspath(output_file)}")
        print("📊 统计信息：")
        print(f"   - 总姓名数：{total_names} 个")
        print(f"   - 排版规则：固定 {fixed_cols} 列")
        print(f"   - 计算行数：每列约 {rows_per_col} 行")
        print("=" * 50)
    except Exception as e:
        print(f"❌ 写入文件失败: {e}")


